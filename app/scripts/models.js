var ArtObject = function(title, department, image, date, period, culture, dimensions, creditline) {
  var self = this;

  self.title = title;
  self.department = department;
  self.imageURL = image;
  self.date = date;
  self.period = period;
  self.culture = culture;
  self.dimensions = dimensions;
  self.creditline = creditline;
};
